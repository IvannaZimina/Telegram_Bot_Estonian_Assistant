"""
Генераторы промптов для OpenAI.

Этот файл формирует текстовые инструкции для перевода, примеров
и морфологического разбора, чтобы бот мог получать ответы в нужном формате.
"""

def make_translation_prompt(user_text: str) -> str:
    return f"""
Ты — детектор языка и переводчик.
Определи, является ли входной текст уже эстонским.

Верни ТОЛЬКО валидный JSON без Markdown, без пояснений и без дополнительного текста.

Формат JSON:
{{
    "is_estonian": true или false,
    "estonian": "эстонский текст",
    "russian": "перевод на русский"
}}

Правила:
- Если текст уже на эстонском, в поле "estonian" верни исходный текст, а в поле "russian" — перевод на русский.
- Если текст не на эстонском, в поле "estonian" верни перевод на эстонский, а поле "russian" оставь пустой строкой.
- Если есть сомнение, считай, что текст не эстонский.

Входной текст: "{user_text}"
"""


def make_examples_prompt(est_word: str, lang: str) -> str:
    translate_to = "русский" if lang == "ru" else "английский"
    return f"""
Создай 3 естественных предложения на ЭСТОНСКОМ со словом "{est_word}".
После каждого предложения ДАЙ перевод на {translate_to}.
Формат ответа без HTML и без тегов:
1. Эстонское предложение
    Перевод
2. Эстонское предложение
    Перевод
3. Эстонское предложение
    Перевод

Никаких тегов <ol>, <li>, <b>, <br> не используй.
"""


def make_forms_prompt(est_word: str) -> str:
    return f"""
Ты — эксперт по эстонской морфологии.
РАБОТАЙ ТОЛЬКО СО СЛОВОМ: '{est_word}'.
Это ЭСТОНСКОЕ слово. НЕ используй русский язык, НЕ склоняй русский ввод.

Твои задачи:

1) Точно определи часть речи слова:
   - nimisõna (существительное)
   - tegusõna (глагол)
   - omadussõna (прилагательное)
   - asesõna (местоимение)

2) Дай только правильные словоформы, существующие в современном эстонском языке.
   НИ В КОЕМ СЛУЧАЕ не придумывай форм.

3) Сначала определи часть речи. Затем выбери только ОДИН подходящий формат:

Если это tegusõna, выведи ТОЛЬКО такой HTML-блок и больше ничего:

<b>Слово:</b> {est_word}
<b>Часть речи:</b> tegusõna

<b>Глагольные формы:</b>
- ma-vorm: ...
- da-vorm: ...
- oleviku vorm (3. pööre): ...
- mineviku vorm: ...
- käskiv kõneviis: ...
- passiiv (если есть): ...

Если это nimisõna, omadussõna или asesõna, выведи ТОЛЬКО такой HTML-блок и больше ничего:

<b>Слово:</b> {est_word}
<b>Часть речи:</b> nimisõna/omadussõna/asesõna

<b>Словоформы:</b>
- nimetav: ...
- omastav: ...
- osastav: ...

Правила:
- Если слово — глагол, не выводи строки nimetav/omastav/osastav.
- Если слово — не глагол, не выводи раздел про глагольные формы.
- Не смешивай оба блока в одном ответе.
- Не добавляй пояснения вне HTML-блока.
- Для слова "olema" используй именно его правильные формы: ma-vorm = olema, da-vorm = olla, oleviku vorm (3. pööre) = on, mineviku vorm = oli, käskiv kõneviis = ole, passiiv = ollakse.
"""
